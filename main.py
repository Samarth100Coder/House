import turtle
turtle.Screen().bgcolor('Red')
t=turtle.Turtle()
t.shape('turtle')
t.color('yellow')
t.penup()
t.goto(-250,180)
t.pendown()
for c in range(2):
   t.forward(500)
   t.right(90)
   t.forward(50)
   t.right(90)
t.penup()
t.goto(-210,130)
t.pendown()
for c in range(2):
   t.forward(420)
   t.right(90)
   t.forward(340)
   t.right(90)
t.penup()
t.goto(-50,-10)
t.pendown()
for c in range(2):
   t.forward(100)
   t.right(90)
   t.forward(200)
   t.right(90)